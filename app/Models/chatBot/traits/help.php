<?php

namespace App\Models\chatBot\traits;

trait help
{
    public function help($edit = 1)
    {
        $this->main->keyBoard->add('Как считается скидка?', 'discount_help');
        $this->main->keyBoard->add('Назад', 'menu');

        $text = implode("\n", [
            '<b>Помощь</b>',
            '',
            'Контакт поддержки: ' . e($this->getHelpContact()),
            '',
            'Как использовать бота:',
            '1. Откройте меню.',
            '2. Перейдите в сценарий запуска.',
            '3. Введите номер и имя клиента.',
            '4. Выберите режим и подтвердите запуск.',
            '',
            'Частые вопросы:',
            '• Как пополнить баланс? Через раздел "Пополнить баланс".',
            '• Как считается скидка? Через активность и будущие правила лояльности.',
            '• Как работает реферальная программа? По вашей ссылке учитываются приглашённые пользователи.',
        ]);

        return $this->sendOrEditMsg($edit, $text);
    }

    public function discount_help($edit = 1)
    {
        $this->main->keyBoard->add('Назад', 'help');

        $text = implode("\n", [
            '<b>Как считается скидка</b>',
            '',
            'На текущем этапе скидка выводится из данных пользователя.',
            'Если отдельное поле скидки ещё не настроено, отображается 0%.',
            'После появления правил лояльности этот экран можно будет расширить без изменения ядра.',
        ]);

        return $this->sendOrEditMsg($edit, $text);
    }
}
